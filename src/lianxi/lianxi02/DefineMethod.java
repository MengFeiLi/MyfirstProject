package lianxi.lianxi02;

public class DefineMethod {

    //    加法
    public static int jiafa(int a, int b) {
        int c = a + b;
        return c;
    }

    //    减法
    public static int jianfa(int a, int b) {
        return a - b;
    }

    //    乘法
    public static void chengfa(int a, int b) {
        System.out.println("a*b = " + a * b);
    }

    //    除法
    public  int chufa(int a,int b) {
        return a / b;
    }


}
