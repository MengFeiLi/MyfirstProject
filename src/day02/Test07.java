package day02;

public class Test07 {
	//默认值
	
	// 0
	static byte m;
	// 0
	static short s;
	// 0
	static int i;
	// 0
	static long l;
	// 0.0
	static double d;
	// 0.0
	static float f;
	// false
	static boolean b;
	// 默认值是一个不可见的字符 \u0000
	static char c;
	
	public static void main(String[] args) {
		System.out.println(m);
		System.out.println(s);
		System.out.println(i);
		System.out.println(l);
		System.out.println(d);
		System.out.println(f);
		System.out.println(b);
		System.out.println(c);
	}
}
