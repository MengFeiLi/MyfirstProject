package day12.T;

public class Num {
	public int plus(int x,int y)
	{
		return x+y;
	}
	public double plus(double x,double y)
	{
		return x+y;
	}
	public float plus(float x,float y)
	{
		return x+y;
	}
	public int minus(int x,int y)
	{
		return x-y;
	}
	public int multiply(int x,int y)
	{
		return x*y;
	}
	public int devide(int x,int y)
	{
		return x/y;
	}
	
}
